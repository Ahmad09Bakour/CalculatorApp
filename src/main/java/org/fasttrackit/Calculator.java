package org.fasttrackit;

public class Calculator {
    public int addNumber(int x, int y) {
        System.out.println("The result of adding the two values is: ");
        return x + y;
    }
    public int subNumber (int a, int b) {
        System.out.println("The result of subtracting the two values is: ");
        return a - b;
    }
    public double mulNumber (double c, double d) {
        System.out.println("The result of multiplying the two values is: ");
        return c * d;
    }
    public double divNumber (double g, double h) {
        System.out.println("The result of dividing the two values is: ");
        return g / h;
    }
}
